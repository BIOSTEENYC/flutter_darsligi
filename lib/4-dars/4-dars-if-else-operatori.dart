/*
        Mavzu: if/else operatori
    if - so'zi agar deb tarjima qilinadi.
    else - so'zi aks holda deb tarjima qilinadi.
    tekshirish uchun ishlatiladi.
Masalan: Agar och bo'lsang ovqatlan, Aks holda bo'kiysan
 */
void main(){
  int yosh = 15;
  if(yosh>12){
    print("Siz o'smir yoshdasiz");
  }
  else{
    print("Bolakaysiz");
  }
}
